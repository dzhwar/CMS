    private void viewAppo(){
        try {
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd-MM-yyyy");
            LocalDateTime now = LocalDateTime.now();
            String d = dtf.format(now).toString();
            
            Statement stm = conn.createStatement();
            
            ResultSet rs = stm.executeQuery("SELECT * FROM appointments WHERE date = '" + d + "' ORDER BY date,time ASC");
            DefaultTableModel tm = (DefaultTableModel)jtblApp.getModel();
            tm.setRowCount(0);

            while(rs.next()){
                Object o[] = {rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                tm.addRow(o);

            }
            
            
        } catch (SQLException ex) {
            //Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }
    }
