    private void jbtnAppFindMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnAppFindMouseClicked
        
        SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
        String from;
        String to;
        String name = jtxtPname.getText();
        
        if (jxdtDateFrom.getDate() == null){
            JOptionPane.showMessageDialog(this, "Select a date please.");
        }
        else{
            from = df.format(jxdtDateFrom.getDate());
            
            if(jxdtDateTo.getDate() == null)
                to = from;
            else
                to = df.format(jxdtDateTo.getDate());

            try {



                Statement stm = conn.createStatement();

                ResultSet rs = stm.executeQuery("SELECT * FROM appointments WHERE date BETWEEN '" + from + "' AND '" +  to + "'"
                        + "OR name = '" + name + "' ORDER BY date, time ASC");
                DefaultTableModel tm = (DefaultTableModel)jtblApp.getModel();
                tm.setRowCount(0);

                while(rs.next()){
                    Object o[] = {rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                    tm.addRow(o);

                }


            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(this, ex);
            }
        }  
        
    }//GEN-LAST:event_jbtnAppFindMouseClicked

