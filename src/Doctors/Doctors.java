    private void jbtnRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRecordActionPerformed
        try {

            String sql = "INSERT INTO records (id,visitDate,note,pres) VALUES(?,?,?,?)";
            PreparedStatement prs = conn.prepareStatement(sql);

            String visitD = jtxtVisitDate.getText();
            String note = jtxtNote.getText();
            String pres = jtxtPre.getText();

            prs.setInt(1, uid);
            prs.setString(2, visitD);
            prs.setString(3, note);
            prs.setString(4, pres);

            prs.execute();

            recordUpdate();
        } catch (SQLException ex) {
            //Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jbtnRecordActionPerformed

