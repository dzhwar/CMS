    private void jtblPatientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblPatientsMouseClicked
        try {
            DefaultTableModel md = (DefaultTableModel)jtblPatients.getModel();
            int row = jtblPatients.getSelectedRow();
            uid = Integer.parseInt(md.getValueAt(row, 0).toString());
            prow = row;
            
            String sql = "SELECT * FROM patient WHERE id = '" + uid + "'";
            PreparedStatement prs = conn.prepareStatement(sql);
            
            ResultSet rs = prs.executeQuery();
            
            while(rs.next()){
                jtxtPid.setText(rs.getString("id"));
                jtxtPname.setText(rs.getString("fullname"));
                jtxtPdob.setText(rs.getString("dob"));
                jtxtPaddress.setText(rs.getString("address"));
                jtxtPphone.setText(rs.getString("phoneno"));
            }
            recordUpdate();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jtblPatientsMouseClicked

    private void jbtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSearchActionPerformed
        try {
            //int id = Integer.parseInt(txtID.getText().toString());
            String n = jtxtSearch.getText();
            DefaultTableModel tm = (DefaultTableModel)jtblPatients.getModel();
            tm.setRowCount(0);
            String sql = "SELECT * FROM patient WHERE fullname LIKE ?";
            PreparedStatement prs = conn.prepareStatement(sql);
            prs.setString(1, n + "%");
            ResultSet rs = prs.executeQuery();
            tm.setRowCount(0);
            
            while(rs.next()){
                Object o[]={rs.getInt("id"),rs.getString("fullname"),rs.getString("dob")};
                tm.addRow(o);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jbtnSearchActionPerformed

